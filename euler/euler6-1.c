//平方的和与和的平方之差

#include <stdio.h>
int main() {

    int sum1 = 5050, sum2 = (2 * 100 * 100 * 100 + 3 * (1 + 100) * 100 - 2 *100) / 6;
    //用 n^3 - (n - 1)^3, 打开最后得到关于sum2平方的和的公式。
    printf("%d\n",sum1 * sum1 - sum2);

    return 0;
}
